export const journalSlugs = postsData.map(p => p.slug)

export async function getJournalPosts(): Promise<JournalPost[]> {
  const allTips = await listResourcesByFolder(TIPS_BASE)

  return postsData.map(post => {
    const prefix = TIP_PREFIX[post.slug]
    const resource = prefix
      ? allTips.find(r => r.publicId.startsWith(prefix))
      : undefined
    return { ...post, coverImage: resource?.url ?? '' }
  })
}
